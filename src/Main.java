public class Main {
    public static void main(String[] args) {
        //Toplam
        int tayi1 = 20;
        int tayi2 = 20;
        int toplam = tayi1 + tayi2;
        System.out.println("Toplama:" + toplam);
        //Çıkarma
        int cayi1 = 55;
        int cayi2 = 20;
        int cikarma = cayi1 - cayi2;
        System.out.println("Çıkarma:" + cikarma);
        //Çarpma
        int caayi1 = 12;
        int caayi2 = 7;
        int carpim = caayi1 * caayi2;
        System.out.println("Çarpma:" + carpim);
        //Bölme
        int bayi1 = 66;
        int bayi2 = 3;
        int bolme = bayi1 / bayi2;
        System.out.println("Bölme:" + bolme);
        //Üçgen
        int yukseklik = 12;
        int taban = 8;
        int alan = (yukseklik * taban) / 2;
        int cevre = taban * 3;
        System.out.println("Üçgenin Alanı:" + alan);
        System.out.println("Üçgenin Çevresi:" + cevre);
        //Daire
        int yaricap = 6;
        int pi = 3;
        int ala = (pi * yaricap) * yaricap;
        int cevr = (pi * yaricap) * 2;
        System.out.println("Dairenin Alanı:" + ala);
        System.out.println("Dairenin Çevresi:" + cevr);


    }
}